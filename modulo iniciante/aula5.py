# Tipo dado bool (boolean)
# Ao questionar alguma coisa no programa
# Só existe duas coisa possiveis:
# Sim (TRUE) ou não (FALSE).
# Existem varios operadores para "questionar"
# Dentre eles o ==, que é um operador logico que
# Que questiona se um valor é igual o outro

print(10 == 10) #Sim => true (verdadeiro)
print(10 == 11) #Não => false (falso)
print(type (10 == 10)) 
print(type (10 == 11)) 
