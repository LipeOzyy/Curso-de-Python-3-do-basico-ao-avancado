# escape (pula o proximo caracter depois \)
print("Pedro \"Rafael\"")

# r
print(r"Pedro \"Rafael\"")

# forma de simplificar esse processo é colocar aspas duplas dentro das simples
print(1, 'Pedro "Rafael"')

# Tipos int e float
# int -> numero inteiro
# positivo ou negativo. int sem sinal é considerado
# positivo
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flutuante
# o tipo flutuante representa qualquer número
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo 
print(1.1)
print(0.0, -1.5)

# A função type mostra o tipoque o python inferiu o valor
print(   type('otavio'))
print(   type(1))
print(   type(0.0), type(-1.1), type(1.1))
